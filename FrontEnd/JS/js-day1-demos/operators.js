// Math
console.log(2 + 3 * 4);
console.log(2 ** 7);
console.log(13 % 7);
console.log();

// Increment/Decrement
let myNumber = 7;
console.log(myNumber);
myNumber++;
console.log(myNumber);
myNumber--;
console.log(myNumber);
console.log();

// Math shorthand
let num1 = 2;
let num2 = 5;
num1 += 4;
num2 *= 5;
console.log('num1:', num1);
console.log('num2:', num2);
console.log();

// String
let hello = 'Hello';
let world = 'World';
let greeting = 'JM';
console.log(hello + world);
console.log(greeting);
greeting += ' Pogi';
console.log(greeting);
console.log();

// Logical
let covidIsOver = false;
console.log(true && true);
console.log(true || false);
console.log(!covidIsOver);
console.log();

// Comparison
console.log(2 == '2');
console.log(2 === '2');
console.log(1 > 3);
console.log();

// Types
console.log(typeof 123);
console.log(typeof 'Hello World');
console.log(new Date() instanceof Date);
console.log(typeof (1 + 1));